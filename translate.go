package main

import (
	"bufio"
	"encoding/binary"
	"errors"
	"go/ast"
	"go/printer"
	"go/token"
	"io"
)

type translator struct {
	ast.File
}

func translate(name string, in io.Reader, out io.Writer) error {
	r := bufio.NewReader(in)
	err := readHeader(r)
	if err != nil {
		return err
	}

	var t translator
	t.Name = ast.NewIdent(name)

	for {
		id, err := peekSectionID(r)
		if err == io.EOF {
			break
		}
		if err != nil {
			return err
		}
		if id == sectionCode {
			break
		}
		if err := skipSection(r, id); err != nil {
			return err
		}
	}

	t.Decls = append(t.Decls, createModuleStruct())

	w := bufio.NewWriter(out)
	w.WriteString("// Code generated by wasm2go. DO NOT EDIT.\n\n")
	printer.Fprint(w, token.NewFileSet(), &t.File)
	return w.Flush()
}

func readHeader(r io.Reader) error {
	var header [8]byte
	if _, err := io.ReadFull(r, header[:]); err != nil {
		return err
	}
	if string(header[:4]) != "\x00asm" {
		return errors.New("invalid magic number")
	}
	if binary.LittleEndian.Uint32(header[4:]) != 1 {
		return errors.New("invalid version")
	}
	return nil
}

func createModuleStruct() ast.Decl {
	return &ast.GenDecl{
		Tok: token.TYPE,
		Specs: []ast.Spec{
			&ast.TypeSpec{
				Name: ast.NewIdent("Module"),
				Type: &ast.StructType{
					Fields: &ast.FieldList{
						List: []*ast.Field{
							{
								Names: []*ast.Ident{ast.NewIdent("memory")},
								Type: &ast.ArrayType{
									Elt: ast.NewIdent("byte"),
								},
							},
						},
					},
				},
			},
		},
	}
}
