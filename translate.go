package main

import (
	"bufio"
	"encoding/binary"
	"errors"
	"fmt"
	"go/ast"
	"go/printer"
	"go/token"
	"io"
	"log"
	"slices"
)

type translator struct {
	in  *bufio.Reader
	out ast.File

	types     []funcType
	functions []funcType
	exports   map[string]export
}

func translate(name string, r io.Reader, w io.Writer) error {
	var t translator

	t.in = bufio.NewReader(r)
	err := readHeader(t.in)
	if err != nil {
		return err
	}

	t.out.Name = ast.NewIdent(name)

	for {
		if err := t.readSection(); err != nil {
			if err == io.EOF {
				break
			}
			return err
		}
	}

	t.out.Decls = append(t.out.Decls, createModuleStruct())

	out := bufio.NewWriter(w)
	out.WriteString("// Code generated by wasm2go. DO NOT EDIT.\n\n")
	printer.Fprint(out, token.NewFileSet(), &t.out)
	return out.Flush()
}

type sectionID byte

const (
	sectionCustom sectionID = iota
	sectionType
	sectionImport
	sectionFunction
	sectionTable
	sectionMemory
	sectionGlobal
	sectionExport
	sectionStart
	sectionElement
	sectionCode
	sectionData
	sectionDataCount
)

func (t *translator) readSection() error {
	id, err := t.in.ReadByte()
	if err != nil {
		return err
	}

	size, err := readLEB128(t.in)
	if err != nil {
		return err
	}

	switch sectionID(id) {
	case sectionType:
		return t.readTypeSection()
	case sectionFunction:
		return t.readFunctionSection()
	case sectionExport:
		return t.readExportSection()
	case sectionCode:
		return t.readCodeSection()
	default:
		log.Printf("skipped section %d", id)
		_, err = t.in.Discard(int(size))
		return err
	}
}

type wasmType byte

const (
	i32 wasmType = 0x7f
	i64 wasmType = 0x7e
	f32 wasmType = 0x7d
	f64 wasmType = 0x7c
)

type funcType struct {
	params  string // the wasmType of parameters
	results string // the wasmType of results
}

func (t *translator) readTypeSection() error {
	numTypes, err := readLEB128(t.in)
	if err != nil {
		return err
	}

	var types []byte
	t.types = make([]funcType, numTypes)
	for i := range t.types {
		form, err := t.in.ReadByte()
		if err != nil {
			return err
		}

		if form != 0x60 {
			fmt.Printf("form: %x\n", form)
			return errors.New("only function types are supported")
		}

		// Parse parameter types.
		n, err := readLEB128(t.in)
		if err != nil {
			return err
		}

		types = slices.Grow(types[:0], int(n))[:n]
		_, err = io.ReadFull(t.in, types)
		if err != nil {
			return err
		}
		t.types[i].params = string(types)

		// Parse result types.
		n, err = readLEB128(t.in)
		if err != nil {
			return err
		}

		types = slices.Grow(types[:0], int(n))[:n]
		_, err = io.ReadFull(t.in, types)
		if err != nil {
			return err
		}
		t.types[i].results = string(types)
	}
	return nil
}

func (t *translator) readFunctionSection() error {
	numFuncs, err := readLEB128(t.in)
	if err != nil {
		return err
	}

	t.functions = make([]funcType, numFuncs)
	for i := range t.functions {
		index, err := readLEB128(t.in)
		if err != nil {
			return err
		}
		t.functions[i] = t.types[index]
	}
	return nil
}

type exportKind byte

const (
	functionExport exportKind = iota
	tableExport
	memoryExport
	globalExport
)

type export struct {
	kind  exportKind
	index int
}

func (t *translator) readExportSection() error {
	numExports, err := readLEB128(t.in)
	if err != nil {
		return err
	}

	var name []byte
	t.exports = make(map[string]export, numExports)
	for range numExports {
		n, err := readLEB128(t.in)
		if err != nil {
			return err
		}

		name = slices.Grow(name[:0], int(n))[:n]
		_, err = io.ReadFull(t.in, name)
		if err != nil {
			return err
		}

		kind, err := t.in.ReadByte()
		if err != nil {
			return err
		}
		index, err := readLEB128(t.in)
		if err != nil {
			return err
		}
		t.exports[string(name)] = export{
			kind:  exportKind(kind),
			index: int(index),
		}
	}
	return nil
}

func (t *translator) readCodeSection() error {
	numFuncs, err := readLEB128(t.in)
	if err != nil {
		return err
	}

	for i := range numFuncs {
		log.Printf("Code for function %d", i)
		_, err := readLEB128(t.in)
		if err != nil {
			return err
		}

		numLocals, err := readLEB128(t.in)
		if err != nil {
			return err
		}
		for range numLocals {
			n, err := readLEB128(t.in)
			if err != nil {
				return err
			}
			typ, err := t.in.ReadByte()
			if err != nil {
				return err
			}
			log.Printf("  Local: %d x %v", n, wasmType(typ))
		}

		for depth := 1; depth > 0; {
			opcode, err := t.in.ReadByte()
			if err != nil {
				return err
			}

			switch opcode {
			case 0x02, 0x03, 0x04: // block, loop, if
				depth++
				bt, err := t.readBlockType()
				if err != nil {
					return err
				}
				var name string
				switch opcode {
				case 0x02:
					name = "block"
				case 0x03:
					name = "loop"
				case 0x04:
					name = "if"
				}
				log.Printf("  %s %#v", name, bt)
			case 0x05: // else
				log.Println("  else")
			case 0x0b: // end
				depth--
				log.Println("  end")
			case 0x0c: // br
				idx, err := readLEB128(t.in)
				if err != nil {
					return err
				}
				log.Printf("  br %d", idx)
			case 0x1b: // select
				log.Println("  select")
			case 0x20: // local.get
				idx, err := readLEB128(t.in)
				if err != nil {
					return err
				}
				log.Printf("  local.get %d", idx)
			case 0x21: // local.set
				idx, err := readLEB128(t.in)
				if err != nil {
					return err
				}
				log.Printf("  local.set %d", idx)
			case 0x22: // local.tee
				idx, err := readLEB128(t.in)
				if err != nil {
					return err
				}
				log.Printf("  local.tee %d", idx)
			case 0x41: // i32.const
				val, err := readSignedLEB128(t.in)
				if err != nil {
					return err
				}
				log.Printf("  i32.const %d", val)
			case 0x4a: // i32.gt_s
				log.Println("  i32.gt_s")
			case 0x6a: // i32.add
				log.Println("  i32.add")
			case 0x6b: // i32.sub
				log.Println("  i32.sub")
			default:
				return fmt.Errorf("unsupported opcode: 0x%x", opcode)
			}
		}
	}
	return nil
}

func (t *translator) readBlockType() (typ funcType, err error) {
	i, err := readSignedLEB128(t.in)
	if err != nil {
		return
	}
	switch i {
	case -1:
		typ.results = string([]wasmType{i32})
	case -2:
		typ.results = string([]wasmType{i64})
	case -3:
		typ.results = string([]wasmType{f32})
	case -4:
		typ.results = string([]wasmType{f64})
	case -64:
		break
	default:
		return t.types[i], nil
	}
	return
}

func readHeader(r io.Reader) error {
	var header [8]byte
	if _, err := io.ReadFull(r, header[:]); err != nil {
		return err
	}
	if string(header[:4]) != "\x00asm" {
		return errors.New("invalid magic number")
	}
	if binary.LittleEndian.Uint32(header[4:]) != 1 {
		return errors.New("invalid version")
	}
	return nil
}

func createModuleStruct() ast.Decl {
	return &ast.GenDecl{
		Tok: token.TYPE,
		Specs: []ast.Spec{
			&ast.TypeSpec{
				Name: ast.NewIdent("Module"),
				Type: &ast.StructType{
					Fields: &ast.FieldList{
						List: []*ast.Field{
							{
								Names: []*ast.Ident{ast.NewIdent("memory")},
								Type: &ast.ArrayType{
									Elt: ast.NewIdent("byte"),
								},
							},
						},
					},
				},
			},
		},
	}
}
