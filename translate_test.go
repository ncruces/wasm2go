func Test_translate(t *testing.T) {
	tests := []string{
		"fib", "loops", "memory", "primes", "recursion", "stack", "table", "trig",
		"regression/select_effect", "regression/store_grow", "regression/tee_self_loop",
	}
	for _, name := range tests {
		t.Run(name, func(t *testing.T) {
			path := "testdata/" + name + "/" + filepath.Base(name)

			in, err := os.Open(path + ".wasm")
			if err != nil {
				t.Fatal(err)
			}
			defer in.Close()

			var out bytes.Buffer
			err = translate(in, &out)
			if err != nil {
				t.Fatal(err)
			}

			err = os.WriteFile(path+".go", out.Bytes(), 0644)
			if err != nil {
				t.Fatal(err)
			}
		})
	}
}

func Test_translateSpecTest(t *testing.T) {
	*nanbox = true
	t.Cleanup(func() { *nanbox = false })

	slices.Sort(skipModules)
	slices.Sort(specHostModules)

	filepath.WalkDir("internal/spectest/", func(path string, d fs.DirEntry, err error) error {
		if err != nil {
			return err
		}
		if d.IsDir() {
			if !*unsafe && strings.HasSuffix(path, "/threads") {
				return filepath.SkipDir
			}
			if strings.HasSuffix(path, "skip") {
				return filepath.SkipDir
			}
			return nil
		}
		if ext := filepath.Ext(path); ext == ".wasm" {
			in, err := os.Open(path)
			if err != nil {
				t.Errorf("%s: %v", path, err)
				return nil
			}
			defer in.Close()

			var out bytes.Buffer
			err = translate(in, &out)
			if err != nil {
				t.Errorf("%s: %v", path, err)
				return nil
			}

			err = os.WriteFile(strings.TrimRight(path, ext)+".go", out.Bytes(), 0644)
			if err != nil {
				t.Errorf("%s: %v", path, err)
				return nil
			}

			err = generateSpecTest(path)
			if err != nil {
				t.Errorf("%s: %v", path, err)
				return nil
			}
		}
		return nil
	})
}

func generateSpecTest(path string) error {
	testDir := filepath.Dir(path)
	suiteDir := filepath.Dir(testDir)
	suiteName := filepath.Base(suiteDir)
	testFile := filepath.Join(testDir, "gen_test.go")
	wasmFile := filepath.Base(path)
	moduleName := strings.TrimSuffix(wasmFile, filepath.Ext(wasmFile))

	if _, found := slices.BinarySearch(skipModules, moduleName); found {
		return nil
	}

	if _, err := os.Stat(filepath.Join(suiteDir, suiteName+".json")); errors.Is(err, fs.ErrNotExist) {
		return nil
	}

	type specFileInfo struct {
		JSONFile   string
		WasmFile   string
		HostModule string
	}

	info := specFileInfo{
		JSONFile: filepath.ToSlash(filepath.Join("..", suiteName+".json")),
		WasmFile: wasmFile,
	}

	if _, found := slices.BinarySearch(specHostModules, moduleName); found {
		info.HostModule = `spectest.Host{T: t}`
	}

	tmpl, err := template.New("spectest").Parse(specTestTemplate)
	if err != nil {
		return err
	}

	var buf bytes.Buffer
	if err := tmpl.Execute(&buf, info); err != nil {
		return err
	}

	src, err := format.Source(buf.Bytes())
	if err != nil {
		return err
	}

	return os.WriteFile(testFile, src, 0644)
}

const specTestTemplate = `// Code generated by wasm2go. DO NOT EDIT.

package wasm2go

import (
	"testing"

	"github.com/ncruces/wasm2go/internal/spectest"
)

func Test(t *testing.T) {
	spectest.TestModule(t, func() any {
		return New({{.HostModule}})
	}, "{{.JSONFile}}", "{{.WasmFile}}")
}
`

