var runGopls = (os.Getenv("RUN_GOPLS") != "")

func Test_translate(t *testing.T) {
	tests := []string{"fib", "loops", "memory", "primes", "recursion", "stack", "table", "trig"}
	*nanbox = false
	for _, name := range tests {
		t.Run(name, func(t *testing.T) {
			path := "testdata/" + name + "/" + name

			in, err := os.Open(path + ".wasm")
			if err != nil {
				t.Fatal(err)
			}
			defer in.Close()

			var out bytes.Buffer
			err = translate(in, &out)
			if err != nil {
				t.Fatal(err)
			}

			gopath := path + ".go"

			err = os.WriteFile(gopath, out.Bytes(), 0644)
			if err != nil {
				t.Fatal(err)
			}

			if runGopls {
				cmd := exec.Command("gopls", "check", "./"+gopath)
				cmd.Stdout = os.Stdout
				cmd.Stderr = os.Stderr
				if err := cmd.Run(); err != nil {
					panic(err)
				}
			}
		})
	}
}

func Test_translateSpecTest(t *testing.T) {
	*nanbox = true
	filepath.WalkDir("spectest/", func(path string, d fs.DirEntry, err error) error {
		if d.IsDir() {
			if !*unsafe && strings.HasSuffix(path, "/threads") {
				return filepath.SkipDir
			}
			return nil
		}
		if ext := filepath.Ext(path); ext == ".wasm" {
			in, err := os.Open(path)
			if err != nil {
				t.Errorf("%s: %v", path, err)
				return nil
			}
			defer in.Close()

			var out bytes.Buffer
			err = translate(in, &out)
			if err != nil {
				t.Errorf("%s: %v", path, err)
				return nil
			}

			gopath := strings.TrimRight(path, ext) + ".go"

			err = os.WriteFile(gopath, out.Bytes(), 0644)
			if err != nil {
				t.Errorf("%s: %v", path, err)
				return nil
			}

			err = generateSpecTest(path)
			if err != nil {
				t.Errorf("%s: %v", path, err)
				return nil
			}

			if runGopls {
				cmd := exec.Command("gopls", "check", "./"+gopath)
				cmd.Stdout = os.Stdout
				cmd.Stderr = os.Stderr
				if err := cmd.Run(); err != nil {
					panic(err)
				}
			}
		}
		return nil
	})
}

func generateSpecTest(path string) error {
	dir := filepath.Dir(path)
	baseDir := filepath.Base(dir)
	wasmFile := filepath.Base(path)
	jsonFile := baseDir + ".json"
	testFile := filepath.Join(dir, "gen_test.go")

	if _, err := os.Stat(filepath.Join(dir, baseDir+".json")); errors.Is(err, fs.ErrNotExist) {
		return nil
	}

	type specFileInfo struct {
		ImportPath string
		JSONFile   string
		WasmFile   string
	}

	info := specFileInfo{
		ImportPath: "github.com/ncruces/wasm2go/" + dir,
		JSONFile:   jsonFile,
		WasmFile:   wasmFile,
	}

	tmpl, err := template.New("spectest").Parse(specTestTemplate)
	if err != nil {
		return err
	}

	var buf bytes.Buffer
	if err := tmpl.Execute(&buf, info); err != nil {
		return err
	}

	src, err := format.Source(buf.Bytes())
	if err != nil {
		return err
	}

	return os.WriteFile(testFile, src, 0644)
}

const specTestTemplate = `// Code generated by wasm2go. DO NOT EDIT.

package wasm2go

import (
	_ "embed"
	"testing"

	"github.com/ncruces/wasm2go/spectest"
)

//go:embed {{.JSONFile}}
var data []byte

func Test(t *testing.T) {
	spectest.Test(t, New(), data, "{{.WasmFile}}")
}
`
